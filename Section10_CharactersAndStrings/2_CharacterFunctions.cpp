/***
 * [Character Functions]
 * #include <cctype>\
 * 
 * 
 * function_name (char)
 * 
 * Functions for testing characters => They evaluate to true or false
 * Functions for converting character case => from upper to lower or lower to uppercase
 * 
 * [Testing characters]
 * 
 * isalpha (c)      |   True if c is a letter
 * isalnum (c)      |   True if c is a letter or digit
 * isdigit (c)      |   True if c is a digit
 * islower (c)      |   True if c is a lowercase letter
 * isprint (c)      |   True if c is a printable character
 * ispunct (c)      |   True if c is a punctuation character
 * isupper (c)      |   True if c is a uppercase letter
 * isspace (c)      |   True if c is a whitespace
 * 
 * [Converting characters]
 * 
 * tolower (c)      |   returns lowercase of c
 * toupper (c)      |   returns uppercase of c
 * ***/